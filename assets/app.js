const supported = ["id", "ja", "en"];

function getInitialLanguage() {
  const stored = localStorage.getItem("tem-language");
  if (supported.includes(stored)) return stored;

  const browserLang = (navigator.languages && navigator.languages[0]) || navigator.language || "en";
  const code = browserLang.toLowerCase().split("-")[0];
  return code === "id" || code === "ja" ? code : "en";
}

function getValue(path, lang) {
  return path.split(".").reduce((obj, key) => obj && obj[key], translations[lang]);
}

function setLanguage(lang) {
  document.documentElement.lang = lang;
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = getValue(node.dataset.i18n, lang);
    if (value) node.textContent = value;
  });
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.classList.toggle("active", button.dataset.lang === lang);
    button.setAttribute("aria-pressed", String(button.dataset.lang === lang));
  });
  localStorage.setItem("tem-language", lang);
}

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => setLanguage(button.dataset.lang));
});

document.getElementById("year").textContent = new Date().getFullYear();
setLanguage(getInitialLanguage());
